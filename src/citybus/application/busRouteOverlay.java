	@Override
	public boolean draw(Canvas canvas, MapView mapView, boolean shadow,
			long when) {
		
		Projection projection = mapView.getProjection();
		Paint paint = new Paint();
		paint.setAntiAlias(true);
		
		for(int j=0; j<8; j++) {
			//initialize for each loop
			if(!checkPreferences(j)) continue;
			double[][] currentLoop = getCurLoop(j);
			setPaintProperties(paint, j);			
			//draw all of the dots for the loop
			for(int i=0; i<currentLoop.length-1; i++) {
				GeoPoint geopoint = new GeoPoint(convertToMicro(currentLoop[i][1]),convertToMicro(currentLoop[i][0]));
				Point pointA = projection.toPixels(geopoint, null);
				geopoint = new GeoPoint(convertToMicro(currentLoop[i+1][1]),convertToMicro(currentLoop[i+1][0]));
				Point pointB = projection.toPixels(geopoint, null);
				if(checkInBounds(pointA, pointB, mapView) != true)
					continue;
				canvas.drawLine(pointA.x, pointA.y, pointB.x, pointB.y, paint);
			}
		}
		return super.draw(canvas, mapView, shadow, when);
	}
	
